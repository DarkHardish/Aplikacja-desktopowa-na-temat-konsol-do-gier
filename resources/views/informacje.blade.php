    <p class="text-2xl l text-justify">Odłączane pady czy też kontrolery ruchu Joy-Con umożliwiają rozrywkę niespotykaną na innych konsolach. 
        Producenci wykorzystują ich możliwości do gier imprezowych, w stylu dawnego Nintendo Wii. 
        Dla fanów gier jednoosobowych z dużą ilością akcji Nintendo ma bardziej „tradycyjny” (i świetnie sprawujący się) Switch Pro Controller.


        Nintendo Switch występuje w trzech wersjach. Którą konsolę wybrać? Oryginalny model ma ekran o przekątnej 6,2 cala. 
        Nowszy model Switch OLED ma 7-calowy ekran OLED z niewielkimi ramkami, które sprawiają, że wydaje się jeszcze większy niż w modelu podstawowym. 
        Ma także poprawiony design i komfort użytkowania. 
        Z kolei wyłącznie przenośny Switch Lite ma 5,5-calowy ekran, wbudowane, niezdejmowalne elementy sterujące, brak podstawki i wyjścia TV, ale jest też znacznie tańszy od większych braci.
        </p>

        <p class="text-3xl font-bold text-center pt-4 ">Podsumowanie </p>
<p class="text-xl text-justify">
    Jeśli jesteś gamerem i chciałbyś grać w najnowsze gry w jak najlepszej jakości na wielkim ekranie, wybierz oczywiście najnowszą generację konsol, czyli PlayStation 5 lub Xbox Series X/S. 
    To obecnie najmocniejsze, choć też najdroższe konsole. Jeśli zaś zależy ci na mobilności i chciałbyś grać niezależnie od tego, czy jesteś w domu, czy poza nim, najlepszą opcją będzie Nintendo Switch. 
    To także dobry wybór dla osób, które lubię gry indie, klasyki takie jak Super Mario Bros czy Zelda i świetną zabawę w gronie znajomych czy rodziny.<br><br>
    Poprzednia generacja konsol Sony i Microsoftu to propozycja dla osób, które nie chcą przeznaczać na wirtualną rozrywkę dużej kwoty, nie zależy im na świetnej jakości obrazu,
    ale lubią pograć w gry na telewizorze – dostępnych jest na nie, i wciąż ukazuje się, wiele ciekawych tytułów, prezentujących się dobrze jakościowo gier (choć nie tak jak na next genach).<br><br>
    Jeśli zamierzasz grać często online ze znajomymi i zastanawiasz się, jaką konsolę warto kupić, wybierz taką, którą mają oni. Opcja cross-play (możliwość jednoczesnego grania w daną grę online, 
    niezależnie od platformy, jaką się ma) staje się coraz popularniejsza, jednak wciąż obejmuje stosunkowo niewiele tytułów.<br><br>
    Na koniec warto jeszcze wspomnieć o zyskujących ostatnio na popularności konsolach retro. To raczej nie alternatywa, ale fajne uzupełnienie dla omówionych wyżej modeli. 
    Jeśli chcecie się dowiedzieć o nich więcej, przeczytajcie nasz artykuł Retro konsole: bitwy na osiem bitów.
</p>

