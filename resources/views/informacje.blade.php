<p class="text-4xl font-bold pb-3">Jaką konsolę kupić? Którą z nich wybrać i dlaczego?</p>
<img src="/assets/images/info-1.jpg" alt="iphone" class="rounded-xl"></a>
<p class="text-2xl pt-3 text-justify">PlayStation 5, Xbox Series X, poprzednie generacje tych konsol do gier czy może Nintendo Switch – zastanawiasz się, jaką konsolę kupić? <br>
    Możliwość wyboru to większa szansa na znalezienie urządzenia jak najlepiej dopasowanego do twoich preferencji i budżetu, ale też utrudnienie decyzji. <br>
    Choć wszystkie konsole zapewniają świetną rozrywkę, to jednak występują między nimi różnice. <br>
    Nasz poradnik pomoże ci zdecydować, jaką konsolę kupić w 2023 roku.</p>
<div class="">
<p class="text-3xl font-bold text-center py-4 ">Istotne cechy, które mogą mieć wpływ na wybór</p>

<p class="text-2xl l text-justify">Wbrew pozorom decyzja, jaką konsolę do gier wybrać, nie musi należeć do najłatwiejszych. Jest całkiem sporo czynników do rozważenia, 
    a także osobiste preferencje, które będą miały wpływ na twój wybór. 
    Oto kilka istotnych cech, które powinieneś wziąć pod uwagę, decydując, którą konsolę wybrać.  </p>

<p class="text-xl font-semibold py-2">Specyfikacja</p>

<p class="text-2xl l text-justify">Procesor, karta graficzna, pamięć RAM, rodzaj dysku mają wpływ na jakość wyświetlanych gier. Jeśli chciałbyś się cieszyć pełną detali grafiką i płynną rozgrywką, 
    powinieneś postawić na konsole nowej generacji Sony PlayStation 5 (Ps5) i Microsoft Xbox Series X (XSX). 
    To najnowsza generacja wyposażona w najmocniejsze podzespoły.</p>

<p class="text-xl font-semibold py-2">Miejsce korzystania</p>

<div class="flex flex-col w-1/2 h-1/4 object-contain mx-auto gap-2">
    <img src="/assets/images/info-5.jpg" class="rounded-xl object-scale-down">
    <img src="/assets/images/info-3.jpg" class="rounded-xl object-scale-down">
</div>
<p class="text-2xl l text-justify">Tutaj mamy na myśli to, czy konsola będzie wykorzystywana tylko stacjonarnie (w domu), czy też zależy ci na mobilności – użytkowaniu jej np. podczas podróży. 
    Tutaj dużą przewagę ma Nintendo Switch, na którym można grać zarówno po podłączeniu do telewizora, jak i poza domem.</p>
<p class="text-xl font-semibold py-2">Biblioteka gier</p>
<p class="text-2xl l text-justify">Konsole Sony, choć Nintendo Switch niewiele im ustępuje, oferują wiele hitowych gier na wyłączność (czyli niedostępnych na innych urządzeniach). 
    Microsoft ze swoim Xboxem zazwyczaj ustępuje im na tym polu, choć ostatnio firma kupiła Bethsedę i Activision Blizzard – producentów i wydawców wielu blockbusterów – więc sytuacja może ulec zmianie. 
    Lista dostępnych gier może być jedną z najważniejszych w przypadku decyzji, jaką konsolę warto kupić.</p>
<p class="text-xl font-semibold py-2">Dodatkowe funckjonalności</p>
<div class="flex flex-col w-1/2 gap-2 object-contain mx-auto">
    <img src="/assets/images/info-6.jpg" class="rounded-xl object-scale-down">
</div>
<p class="text-2xl l text-justify">Obecne konsole służą nie tylko do grania – to wszechstronne urządzenia zapewniające wielowymiarową rozrywkę. 
    Mogą także odtwarzać filmy na Blu-ray, aplikacje VOD (jak Netflix czy YouTube), pozwalać na przeglądanie internetu, 
    korzystanie z VR czy zapewnić aktywności ruchowe dzięki wykorzystaniu specjalnych kontrolerów.</p>
<p class="text-xl font-semibold py-2">Kompatybilność wsteczna</p>
<p class="text-2xl l text-justify">Oznacza możliwość uruchomienia gier z poprzedniej generacji konsol. Na Ps5 i Xbox serii X/S, możesz zagrać w gry z, odpowiednio, PS4 oraz Xbox One X/S, czyli poprzednich generacji. 
    Te ostatnie z kolei takiej możliwości nie mają. Podobnie ma się sprawa z Nintendo Switch – nie zagrasz na nim w gry z Nintendo Wii czy 3DS.</p>
</div>
<p class="text-3xl font-bold text-center pt-4 ">PlayStation 5 i Xbox Series X - którą wybrać ? </p>
<div class="flex flex-col w-1/2 h-1/4 object-contain mx-auto gap-2 text-center font-bold pb-4">
    <img src="/assets/images/Ps5_3.png" class="rounded-xl object-scale-down">
    <p>PlayStation 5</p>
    <img src="/assets/images/Xbox_Series_X_1.png" class="rounded-xl object-scale-down">
    <p>Xbox Series X</p>
</div>
<p class="text-2xl l text-justify">PlayStation 5 i Xbox Series X to najnowsze generacje konsol, tzw. next geny – wyprodukowane przez firmy Sony oraz Microsoft. Premierę miały pod koniec 2020 roku. 
    Obie to urządzenia stacjonarne z kompatybilnością wsteczną i bardzo mocnymi podzespołami.
     Dlatego też mogą obsługiwać natywną rozdzielczość 4K i wyświetlać nawet 120 kl./s, dzięki czemu gry (nawet te bardzo dynamiczne tytuły) działają na nich niezwykle płynnie.<br><br>

    Czasami wymaga to jednak pójścia na kompromis i obniżenia jakości grafiki – jest to wybór pomiędzy trybami wydajności a jakości. 
    Niesamowita szczegółowość obrazu, jaką oferują oba urządzenia, to także efekt wykorzystywania technologii ray tracing. 
    Nie można również zapomnieć o tym, że Ps5 i XSX zostały wyposażone w szybkie dyski SSD (to pierwsza generacja, która ma dyski tego typu), 
    co pozwoliło na znaczne przyśpieszenie wczytywania gier – w porównaniu z poprzednią generacją różnica jest naprawdę duża.</p>
  
<p class="text-2xl l text-justify">W decyzji, jaką konsolę kupić, pomogą oczywiście specyficzne cechy danej konsoli, do których teraz przejdziemy. 
    Zacznijmy od PlayStation 5, która cieszy się w naszym kraju dużą popularnością. Tym, co przede wszystkim daje Ps5 przewagę nad Xbox Series X, są gry. 
    PlayStation zawsze słynęło z dostępności hitowych exclusivów. 
    Przykładami są takie bestsellery, jak serie God of War, Uncharted czy też obie części The Last of Us, ale jest ich (i będzie) więcej.<br>

    Ps5 to także pad DualSense z technologią haptyczną – potrafi ona między innymi, przy użyciu wibracji, pomóc graczowi przewidzieć, 
    z której strony może nastąpić atak – a także szybki i świetnie zaprojektowany interfejs użytkownika. 
    Dodajmy jeszcze, że do PlayStation 5 można podłączyć immersyjne gogle PlayStation VR i w 100% zanurzyć się w grze. 
    Wszystko to warto wziąć pod uwagę, gdy zastanawiasz się, jaką konsolę wybrać.<br>
    
    Jeśli zaś chodzi o Xbox Series X, to – biorąc pod uwagę zbliżone parametry techniczne – jej główną siłą jest abonament Xbox Game Pass oraz szersza kompatybilność wsteczna w porównaniu z Ps5. 
    Za miesięczną opłatą Xbox Game Pass daje graczowi dostęp do setek gier. Choć nie ma tak mocnych exclusivów jak urządzenie Sony. 
    Od niedawna w ramach usługi Game Pass Ultimate dostępna jest transmisja strumieniowa, dzięki której granie w gry z XSX można kontynuować na urządzeniach mobilnych. 
    <br>Jest to jakaś namiastka konsoli przenośnej, jednak do Nintendo Switch w tym względzie jej daleko.<br>
    Kompatybilność wsteczna w Xbox Series X sięga nie tylko poprzedniej generacji (Xbox One), ale również dwie generacje wstecz, do Xbox 360.
     W odróżnieniu od PS5 najnowsze urządzenie Microsoftu ma wsparcie dla Dolby Vision i Dolby Atmos, co warto wziąć pod uwagę, gdy zastanawiasz się, którą konsolę wybrać.
      Dodajmy też, że jest zadziwiająco cicha.</p>

<p class="text-3xl font-bold text-center pt-4 ">Poprzednie generacje konsol Sony oraz Microsoft </p>
<p class="text-2xl l text-justify">
Mimo że od premiery PS5 i Xbox Series X/S minął już ponad rok, to poprzednie generacje konsol Sony i Microsoftu cały czas mogą być dobrym wyborem, jeśli zastanawiasz się, jaką konsolę kupić w 2022 roku. 
Choć od ich premiery minęło już bardzo dużo czasu, to wciąż mogą się pochwalić dobrą jakością wyświetlanych treści. Ich przewaga nad next genami to, na razie, większa liczba dostępnych gier.
 I dostępność samej konsoli, jeśli mówimy o nowym sprzęcie Sony – PlayStation 5 wciąż jest towarem deficytowym, choć Japończycy obiecują, że niedługo się to zmieni.</p><br><br>
    <div class="flex flex-col w-1/2 h-1/4 object-contain mx-auto">
    <img src="/assets/images/Ps4_1.png" class="rounded-xl">
    </div>
<p class="text-2xl l text-justify">
PS4 Pro (bo na tej wersji się skupimy) również może wyświetlać rozdzielczość 4K i obsługuje HDR. 
Co prawda gry nie będą się prezentować tak okazale i działać tak płynnie jak w przypadku PS5 – wiele z nich zostanie do tej rozdzielczości przeskalowanych. Konsola nie ma wystarczającej mocy, by utrzymać natywną rozdzielczość 4K i uruchomić wymagającą grę ze stałą liczbą klatek na sekundę. 
PlayStation 4 Pro nie ma też wbudowanego odtwarzacza płyt Ultra HD Blu-ray – odtwarza standardowe płyty Blu-ray i DVD, a także umożliwia strumieniowanie w 4K z kompatybilnych serwisów. 
Zaletą urządzenia są oczywiście exclusivy.<br><br>
    
Podczas gdy Sony cały czas produkuje PlayStation 4 i PS4 Pro, to Microsoft zaprzestał produkcji Xbox One S i Xbox One X, trudno więc je dostać w regularnej sprzedaży.
 Ten drugi model to najmocniejsza konsola poprzedniej generacji. Pozwala grać w gry w natywnej rozdzielczości 4K w 60 kl./s., obsługuje także HDR10.</p>
 <div class="flex flex-col w-1/2 h-1/4 object-contain mx-auto py-4">
    <img src="/assets/images/Xbox_One_X.png" class="rounded-xl">
    </div>
<p class="text-2xl l">Poza tym Xbox One X wyróżnia się wbudowanym napędem, który pozwala odtwarzać filmy z dysków Ultra HD Blu-ray. Podobnie zresztą jak Xbox One S (to ich przewaga nad równorzędną generacją sprzętu Sony), który jednak nie obsługuje natywnego 4K (a także HDR10), tylko musi go skalować, co odbija się na jakości wyświetlanego obrazu. Silną stroną obu konsol Microsoftu jest też oczywiście duża biblioteka gier dostępna dzięki abonamentowi Game Pass.

</p>

<p class="text-3xl font-bold text-center pt-4 ">Jedyna w swoim rodzaju - Nintendo Switch </p>

<p class="text-2xl l text-justify">Nintendo Switch to jedyna taka propozycja na rynku. Jeśli trudno ci zdecydować, jaką konsolę wybrać – stacjonarną czy przenośną – to urządzenie jest dla ciebie. 
    Nintendo Switch łączy mobilność z możliwością podłączenia do telewizora i grania na dużym ekranie. 
    Konsola składa się z małego tabletu z dołączonymi kontrolerami – w ten sposób możesz grać poza domem –  ale możesz ją również podłączyć do telewizora za pomocą stacji dokującej.<br><br>

    Switch nie wyświetla rozdzielczości 4K i nie obsługuje wielu aplikacji do strumieniowego przesyłania wideo. 
    Ale oferuje użytkownikom rozrywkę dostarczającą mnóstwa emocji. Nie tylko pojedynczemu graczowi, ale rodzinie czy grupie znajomych. Na konsolę Nintendo nie jest dostępna większość bestsellerów (choć to też się powoli zmienia, jakiś czas temu konsola „dostała” swoją wersję Wiedźmina 3: Dziki Gon), oferuje za to graczom ogromną bibliotekę wciągających gier niezależnych i akcesoria do gier ruchowych czy np. fitnessu. 
    Na Switch dostępne są za to klasyczne i legendarne już serie gier, takie jak Mario, Zelda i Pokémon.</p>
